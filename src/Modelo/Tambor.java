package Modelo;

/**
 *
 * @author EQUIPO-PC
 */
public class Tambor {
    int idTambor;
    String nombreTambor;
    int estatus;

    public int getIdTambor() {
        return idTambor;
    }

    public void setIdTambor(int idTambor) {
        this.idTambor = idTambor;
    }

    public String getNombreTambor() {
        return nombreTambor;
    }

    public void setNombreTambor(String nombreTambor) {
        this.nombreTambor = nombreTambor;
    }

    public int getEstatus() {
        return estatus;
    }

    public void setEstatus(int estatus) {
        this.estatus = estatus;
    }
}
